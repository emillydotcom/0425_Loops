// Atividade 1
public class Multiplicacao5 {
    public static void main(String[] args) {

        // Loop de 1 até 10
        for (int i = 1; i <= 10; i++) {
            int resultado = 5 * i; 
            // Multiplicando 5 pelo número atual (i)
            System.out.println("5 x " + i + " = " + resultado); 
        }
    }
}
